""" Simple Module with simple function """


def add(a: float, b: float) -> float:
    """ Sum of two Decimal/Fraction/Floating
    point number

    Arguments:
        a:  a floating point number
        b:  a floating point number

    Returns: The sum of the two floating point numbers
    """

    return a + b
